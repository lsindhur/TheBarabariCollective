//implicit type conversion


//addition operator => string and number =>string conversion
console.log("4"+5); //45 

//4 is a string
//now 5 will also be treated as a string ///=> implicit type conversion

//explicit conversion

console.log(Number(4)+5); //9

//any other operator //string and number => number conversion
console.log("5"-4); //1 

console.log("52"+2+2); //5222
console.log(2+2+"52"); //2252 //452